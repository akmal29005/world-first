// Get regions for a specific country
export const getRegionsForCountry = (country: string): string[] => {
    return REGIONS_BY_COUNTRY[country] || [];
};

// Check if a region is valid for a country
export const isValidRegion = (country: string, region: string): boolean => {
    const regions = getRegionsForCountry(country);
    if (regions.length === 0) return true; // If country not in list, allow any region
    return regions.some(r => r.toLowerCase() === region.toLowerCase());
};
